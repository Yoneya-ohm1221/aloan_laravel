    1.3 ประโยชน์โดยชอบด้วยกฎหมายของบริษัท<br>
    บริษัทจะอ้างอิงฐานประโยชน์โดยชอบด้วยกฎหมาย โดยคำนึงถึงประโยชน์ของบริษัทหรือของบุคคลอื่นกับสิทธิขั้นพื้นฐานในข้อมูลส่วนบุคคลของท่านที่บริษัทจะเก็บรวบรวม ใช้ หรือเปิดเผย ซึ่งรวมถึงแต่ไม่จำกัดเฉพาะวัตถุประสงค์ดังต่อไปนี้<br>
    ก) บริหารกิจการของบริษัท และของบริษัทในกลุ่มธุรกิจทางการเงิน (เช่น กำกับตรวจสอบ บริหารจัดการและการวิเคราะห์ความเสี่ยง เฝ้าระวัง ป้องกัน ระบุ และตรวจสอบการทุจริต การฟอกเงิน การก่อการร้าย การประพฤติโดยมิชอบ หรือการก่ออาชญากรรมอื่น ๆ ซึ่งรวมถึงแต่ไม่จำกัดเพียงการตรวจสอบความน่าเชื่อถือของบุคคลใด ๆ ที่เกี่ยวข้องกับลูกค้าองค์กรธุรกิจของบริษัท) แม้อาจเป็นกรณีที่เจ้าหน้าที่หรือเจ้าหน้าที่กำกับดูแลตามกฎหมายไม่ได้กำหนดให้ต้องดำเนินการ ทั้งนี้ รวมถึงการระบุตัวตนของท่านเพื่อป้องกันอาชญากรรมเหล่านั้น<br>
    ข) บริหารจัดการความสัมพันธ์ระหว่างบริษัทและท่าน (เช่น ดูแลลูกค้า ประเมินความพึงพอใจ จัดการข้อร้องเรียน)<br>
    ค) พัฒนาและปรับปรุงผลิตภัณฑ์และ/หรือบริการ รวมทั้งระบบงานต่าง ๆ ของบริษัท เพื่อยกระดับมาตรฐานการให้บริการของบริษัท และ/หรือเพื่อประโยชน์สูงสุดในการตอบสนองความต้องการของท่าน<br>
    ง) ดำเนินการเพื่อความต่อเนื่องทางธุรกิจ (business continuity) ของบริษัท<br>
    จ) รับมือกับข้อเรียกร้องและข้อพิพาท ซึ่งรวมถึง การดำเนินการแก้ไขข้อพิพาท การเริ่มดำเนินการ การใช้สิทธิ หรือการโต้แย้งข้อเรียกร้องทางกฎหมาย<br>
    ฉ) ติดต่อท่านก่อนที่ท่านจะเข้าทำสัญญาที่เกี่ยวข้องกับบริษัท<br>
    ช) ประเมินความเหมาะสมและคุณสมบัติ การเสนอข้อเรียกร้องเพื่อการเสนอราคาและการประกวดราคา และการเข้าทำสัญญาที่เกี่ยวข้องกับท่าน<br>
    ซ) ป้องกันความเสี่ยงด้านความปลอดภัย เช่น การเฝ้าติดตามข้อมูลการใช้เครือข่าย (network activity logs) การระบุเหตุการณ์เกี่ยวกับความปลอดภัย (security incidents) การดำเนินการสอบสวนเกี่ยวกับความปลอดภัยของข้อมูล และการป้องกันการหลอกลวง การทุจริตและการปฏิบัติผิดกฎหมาย<br>
    ฌ) ปฏิบัติตามกฎหมายต่างประเทศ<br>
    ญ) บริหารโครงสร้างพื้นฐานของบริษัท การตรวจสอบภายใน การควบคุมภายใน การดำเนินธุรกิจ และการปฏิบัติตามนโยบายและกระบวนการของบริษัท ซึ่งรวมถึงการดำเนินการเกี่ยวกับการควบคุมความเสี่ยง ความปลอดภัย การตรวจสอบบัญชี การเงินและการบัญชี และการดำเนินการเพื่อความต่อเนื่องทางธุรกิจ (business continuity) ของบริษัท<br>
    ฎ) ทำวิจัย วางแผน และทำการวิเคราะห์ทางสถิติ เช่น พฤติกรมการชำระเงินคืนของท่าน การวิเคราะห์ข้อมูล (data analytics) การประเมิน การทำแบบสอบถามและรายงานเกี่ยวกับบริการของบริษัทและพฤติกรรมของท่าน<br>
    ฏ) อำนวยความสะดวกแก่การตรวจสอบทางบัญชีซึ่งกระทำโดยผู้สอบบัญชี หรือการรับบริการจากที่ปรึกษากฎหมายที่ถูกแต่งตั้งโดยบริษัทหรือท่าน<br>
    ฐ) ในกรณีที่มีการขาย การโอน การควบกิจการ การฟื้นฟูกิจการ หรือเหตุการณ์ในทำนองเดียวกัน บริษัทอาจเปิดเผยหรือโอนข้อมูลส่วนบุคคลของท่านให้แก่บุคคลที่สามรายใดรายหนึ่งหรือหลายรายซึ่งมีส่วนเกี่ยวข้องกับธุรกรรมนั้น ๆ<br>
    ฑ) ดำรงไว้และปรับปรุงรายชื่อและสมุดรายนามของลูกค้าให้เป็นบัจจุบัน (ซึ่งรวมถึงข้อมูลส่วนบุคคลของลูกค้าท่าน) และเก็บข้อมูลติดต่อและเอกสารที่เกี่ยวข้องซึ่งอาจมีการอ้างถึงท่านในเอกสารเหล่านั้น<br>
    ฒ) ปฏิบัติตามเหตุผลทางธุรกิจที่ต้องดำเนินการ เช่น การจัดการ การอบรม การตรวจสอบบัญชี การรายงาน การควบคุมหรือบริหารความเสี่ยง การวิเคราะห์และวางแผนสถิติและแนวโน้ม หรือกิจกรรมอื่น ๆ ที่คล้ายคลึงกัน การปฏิบัติการควบคุมทางธุรกิจเพื่อให้ธุรกิจสามารถดำเนินการได้และทำให้บริษัทสามารถระบุและแก้ไขปัญหาในระบบ IT เพื่อทำให้เกิดความปลอดภัยกับระบบของบริษัท ทำการพัฒนา ปฏิบัติการ ดำเนินการ และบำรุงรักษาระบบ IT ของบริษัท<br>
    ณ) ดำเนินการพัฒนาแบบจำลองการป้องกันการทุจริต การพัฒนาแบบจำลองการประเมินความเสี่ยงและความน่าเชื่อถือทางด้านการเงิน การพัฒนาแบบจำลองการเรียกเก็บเงิน การพัฒนาแบบจำลองด้านรายได้ และติดตามผลของแบบจำลองความเป็นไปได้ในการอนุมัติ (propensity to approve)
    </p>
    <p>
    1.4 ความยินยอมของท่าน<br>
    ในบางกรณี บริษัทอาจมีการขอความยินยอมจากท่านในการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่านเพื่อให้ท่านได้รับประโยชน์สูงสุด และ/หรือ เพื่อให้บริษัทสามารถให้บริการที่ตอบสนองต่อความต้องการของท่าน ซึ่งรวมถึงแต่ไม่จำกัดเฉพาะวัตถุประสงค์ดังต่อไปนี้<br>
    ก) มีความจำเป็นต้องเก็บรวบรวม ใช้และเปิดเผยข้อมูลส่วนบุคคลอ่อนไหว (sensitive personal data) (เช่น ใช้ข้อมูลชีวภาพ (biometric data) เช่น ข้อมูลการจดจำใบหน้า (face recognition) และ liveness หรือ ภาพถ่ายบัตรประจำตัวประชาชนของท่าน (ซึ่งในบัตรดังกล่าวจะมีข้อมูลส่วนบุคคลอ่อนไหวของท่าน ได้แก่ ศาสนา และ/หรือกรุ๊ปเลือดอยู่) ในการพิสูจน์และยืนยันตัวตนของท่านก่อนการทำธุรกรรม กระบวนการทำความรู้จักลูกค้า (KYC) และกระบวนการเรียกเก็บเงิน)<br>
    ข) เก็บรวบรวม ใช้ และร้องขอข้อมูลส่วนบุคคลของท่านจากบุคคลที่สามตามวัตถุประสงค์ที่ระบุไว้ในแบบขอความยินยอมที่เราอาจร้องขอจากท่าน<br>
    ค) เก็บรวบรวมและใช้ข้อมูลส่วนบุคคลและข้อมูลใด ๆ ของท่านไปวิจัยและวิเคราะห์เพื่อประโยชน์ในการพัฒนาผลิตภัณฑ์และบริการที่ตอบสนองต่อความต้องการของท่าน และ/หรือ ติดต่อท่านเพื่อเสนอผลิตภัณฑ์ บริการ และสิทธิประโยชน์ที่เหมาะสมแก่ท่าน<br>
    ง) เปิดเผยข้อมูลส่วนบุคคลและข้อมูลใด ๆ ของท่าน ให้แก่ธนาคารไทยพาณิชย์ จำกัด (มหาชน) และบริษัทในกลุ่มธุรกิจทางการเงินของธนาคาร เพื่อวัตถุประสงค์ดังนี้ (1) วิจัยและวิเคราะห์ข้อมูลส่วนบุคคลและข้อมูลใด ๆ ของท่านเพื่อประโยชน์สูงสุดในการพัฒนาผลิตภัณฑ์และบริการที่ตอบสนองความต้องการของท่านอย่างแท้จริง และ (2) ติดต่อท่านเพื่อเสนอผลิตภัณฑ์ บริการ และสิทธิประโยชน์ที่เหมาะสมแก่ท่านโดยเฉพาะ
    </p>
    <p>
    1.5. ฐานทางกฎหมายอื่น ๆ<br>
    นอกเหนือไปจากฐานทางกฎหมายข้างต้น บริษัทอาจเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่านภายใต้ฐานทางกฎหมายอื่นดังต่อไปนี้<br>
    ก) จัดทำเอกสารประวัติศาสตร์หรือจดหมายเหตุเพื่อประโยชน์สาธารณะ หรือที่เกี่ยวกับการศึกษาวิจัยหรือสถิติ<br>
    ข) ป้องกันหรือระงับอันตรายต่อชีวิต ร่างกาย หรือสุขภาพของบุคคล<br>
    ค) เป็นการจำเป็นเพื่อการปฏิบัติหน้าที่ในการดำเนินภารกิจเพื่อประโยชน์สาธารณะหรือเพื่อการใช้อำนาจของพนักงานเจ้าหน้าที่
    หากข้อมูลส่วนบุคคลที่บริษัทได้รับจากท่านเป็นข้อมูลที่บริษัทต้องได้มาตามภาระหน้าที่ตามกฎหมายของบริษัทหรือเพื่อการเข้าทำสัญญากับท่าน บริษัทอาจไม่สามารถให้ (หรือให้ต่อไป) ผลิตภัณฑ์และบริการทั้งหมดหรือบางส่วนของบริษัทแก่ท่านได้หากท่านไม่ได้ให้ข้อมูลส่วนบุคคลเหล่านั้นแก่บริษัทเมื่อบริษัทร้องขอ
    </p>
    <p>
    2. แหล่งที่มาของข้อมูลส่วนบุคคลของท่าน<br>
    โดยทั่วไปแล้วบริษัทจะเก็บรวบรวมข้อมูลส่วนบุคคลจากท่านโดยตรง (เช่น ผ่านแอปพลิเคชันบนอุปกรณ์เคลื่อนที่ (mobile applications) และศูนย์บริการลูกค้า) แต่ในบางกรณีบริษัทอาจได้ข้อมูลส่วนบุคคลของท่านมาโดยอ้อม จากแหล่งอื่น (เช่น สื่อสังคมออนไลน์ (social media) แพลตฟอร์มออนไลน์ของบุคคลที่สาม หรือ แหล่งข้อมูลสาธารณะอื่น ๆ) และผ่านทางธนาคารไทยพาณิชย์ จำกัด (มหาชน) บริษัทในเครือ ผู้ให้บริการ พันธมิตรทางธุรกิจ เจ้าหน้าที่และหน่วยงานผู้มีอำนาจตามกฎหมาย หรือบุคคลที่สาม (เช่น ผู้แทนของท่าน นายจ้าง ผู้สนับสนุน (sponsor) และบุคคลที่สามที่มีบทบาทในการให้บริการแก่ท่าน หรือบุคคลใด ๆ ที่ดำเนินการในนามของบุคคลเหล่านั้นเองซึ่งอาจให้ข้อมูลเกี่ยวกับท่านแก่บริษัท) ซึ่งบริษัทจะดำเนินการให้เป็นไปตามที่ พระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล หรือกฎหมายที่เกี่ยวข้อง
    </p>
    <p>
    3. สิทธิตามกฎหมายของท่าน<br>
    พระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคลมีวัตถุประสงค์เพื่อให้ข้อมูลส่วนบุคคลของท่านอยู่ในความควบคุมของท่านได้มากขึ้น โดยท่านสามารถเริ่มใช้สิทธิตามพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคลเมื่อบทบัญญัติในส่วนที่เกี่ยวกับสิทธิของเจ้าของข้อมูลส่วนบุคคลมีผลใช้บังคับ ซึ่งมีรายละเอียดดังต่อไปนี้
    </p>
    <p>
    3.1 สิทธิในการเข้าถึงและขอรับสำเนาข้อมูลส่วนบุคคลของท่าน<br>
    ท่านมีสิทธิเข้าถึงและได้รับสำเนาข้อมูลส่วนบุคคลของท่านที่บริษัทมีอยู่ เว้นแต่กรณีที่บริษัทมีสิทธิปฏิเสธคำขอของท่านตามกฎหมายหรือคำสั่งศาล หรือกรณีที่คำขอของท่านจะมีผลกระทบที่อาจก่อให้เกิดความเสียหายต่อสิทธิและเสรีภาพของบุคคลอื่น
    </p>
    <p>
    3.2 สิทธิในการขอแก้ไขข้อมูลส่วนบุคคลของท่าน<br>
    ท่านมีสิทธิขอให้บริษัททำการแก้ไข หรือปรับปรุงข้อมูลส่วนบุคคลของท่านที่ไม่ถูกต้องหรือไม่ครบถ้วน
    </p>
    <p>
    3.3 สิทธิในการขอให้ลบข้อมูลส่วนบุคคลของท่าน<br>
    ท่านมีสิทธิขอให้บริษัทลบหรือทำลายข้อมูลของท่าน หรือทำให้ข้อมูลของท่านเป็นข้อมูลที่ไม่สามารถระบุตัวตนของท่านได้ เว้นแต่กรณีที่บริษัทจะมีเหตุอันชอบด้วยกฎหมายในการปฏิเสธคำขอของท่าน
    </p>
    <p>
    3.4 สิทธิในการขอให้ระงับการใช้ข้อมูลส่วนบุคคลของท่าน<br>
    ท่านมีสิทธิขอให้บริษัทระงับการใช้ข้อมูลส่วนบุคคลของท่านในบางกรณี เช่น บริษัทอยู่ระหว่างการตรวจสอบคำขอใช้สิทธิแก้ไขข้อมูลส่วนบุคคลหรือคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่าน หรือท่านขอให้บริษัทระงับการใช้ข้อมูลส่วนบุคคลแทนการลบหรือทำลายข้อมูลส่วนบุคคลที่ไม่มีความจำเป็นอีกต่อไป เนื่องจากท่านมีความจำเป็นต้องขอให้เก็บรักษาข้อมูลส่วนบุคคลของท่านไว้เพื่อใช้ในการก่อตั้งสิทธิเรียกร้องตามกฎหมาย  การปฏิบัติตามหรือการใช้สิทธิเรียกร้องตามกฎหมาย  หรือการยกขึ้นต่อสู้สิทธิเรียกร้องตามกฎหมาย
    </p>
    <p>
    3.5 สิทธิในการคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่าน<br>
    ท่านมีสิทธิคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่านในกรณีที่บริษัทดำเนินการภายใต้ฐานประโยชน์โดยชอบด้วยกฎหมาย หรือเพื่อวัตถุประสงค์เกี่ยวกับการตลาดแบบตรงหรือการศึกษาวิจัยทางวิทยาศาสตร์ ประวัติศาสตร์ หรือสถิติ เว้นแต่กรณีที่บริษัทมีเหตุในการปฏิเสธคำขอของท่านโดยชอบด้วยกฎหมาย เช่น บริษัทสามารถแสดงให้เห็นว่าการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่านมีเหตุอันชอบด้วยกฎหมายยิ่งกว่า หรือเพื่อการก่อตั้งสิทธิเรียกร้องตามกฎหมาย การปฏิบัติตามหรือการใช้สิทธิเรียกร้องทางกฎหมาย หรือเพื่อประโยชน์สาธารณะของบริษัท
    </p>
    <p>
    3.6 สิทธิในการขอรับหรือขอให้ส่งหรือโอนข้อมูลส่วนบุคคลของท่าน<br>
    ท่านมีสิทธิขอรับข้อมูลส่วนบุคคลของท่านในกรณีที่บริษัทสามารถทำให้ข้อมูลนั้นอยู่ในรูปแบบที่สามารถอ่านหรือใช้งานได้โดยทั่วไปด้วยเครื่องมือหรืออุปกรณ์ที่ทำงานได้โดยอัตโนมัติและสามารถใช้หรือเปิดเผยข้อมูลส่วนบุคคลได้ด้วยวิธีการอัตโนมัติ รวมทั้งมีสิทธิขอให้บริษัทส่งหรือโอนข้อมูลส่วนบุคคลของท่านไปยังบุคคลภายนอก หรือขอรับข้อมูลส่วนบุคคลที่บริษัทได้ส่งหรือโอนไปยังบุคคลภายนอก เว้นแต่บริษัทไม่สามารถทำได้โดยสภาพทางเทคนิค หรือบริษัทมีเหตุในการปฏิเสธคำขอของท่านโดยชอบด้วยกฎหมาย
    </p>
    <p>
    3.7 สิทธิในการขอถอนความยินยอม<br>
    ท่านมีสิทธิขอถอนความยินยอมที่ท่านได้ให้ไว้กับบริษัทเมื่อใดก็ได้ ตามขั้นตอนและวิธีการที่บริษัทกำหนด เว้นแต่โดยสภาพไม่สามารถถอนความยินยอมได้ การถอนความยินยอมของท่านจะไม่ส่งผลกระทบต่อการเก็บรวบรวม  ใช้  หรือเปิดเผยข้อมูลส่วนบุคคลที่ท่านได้ให้ความยินยอมไปแล้วโดยชอบ
    หากท่านถอนความยินยอม บริษัทอาจไม่สามารถให้บริการบางประการแก่ท่าน (หรือให้บริการต่อเนื่องบางประการแก่ท่าน)
    </p>
    <p>
    3.8 สิทธิในการร้องเรียน<br>
    ท่านมีสิทธิร้องเรียนต่อคณะกรรมการคุ้มครองข้อมูลส่วนบุคคลหรือสำนักงานคณะกรรมการคุ้มครองข้อมูลส่วนบุคคล หากบริษัทกระทำการอันเป็นการไม่ปฏิบัติตามพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล
    </p>
    <p>
    4. การเปิดเผยข้อมูลส่วนบุคคลของท่าน<br>
    บริษัทอาจเปิดเผยข้อมูลส่วนบุคคลของท่านต่อบุคคลหรือองค์กรดังต่อไปนี้ ภายใต้หลักเกณฑ์ของพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล<br>
    ก) บริษัทแม่ บริษัทในกลุ่มธุรกิจทางการเงินของบริษัทแม่ พันธมิตรทางธุรกิจ และ/หรือ บุคคลอื่นใดที่บริษัทมีนิติสัมพันธ์ด้วย รวมถึงกรรมการ ผู้บริหาร พนักงาน ลูกจ้าง ผู้รับจ้าง ตัวแทน ที่ปรึกษาของบริษัท และ/หรือ ของบุคคลดังกล่าว<br>
    ข) หน่วยงานของรัฐ และ/หรือ หน่วยงานที่มีหน้าที่กำกับดูแลบริษัท เช่น ธนาคารแห่งประเทศไทย สำนักงานคณะกรรมการกำกับหลักทรัพย์และตลาดหลักทรัพย์ กระทรวงดิจิทัลเพื่อเศรษฐกิจและสังคม สำนักงานป้องกันและปราบปรามการฟอกเงิน กรมสรรพากร บริษัท ข้อมูลเครดิตแห่งชาติ จำกัด<br>
    ค) คู่ค้า ตัวแทน หรือองค์กรอื่น เช่น สมาคมวิชาชีพที่บริษัทเป็นสมาชิก ผู้ตรวจสอบบัญชีอิสระ ศูนย์รับฝากหลักทรัพย์ คลังเก็บเอกสาร สถาบันการเงินต่างประเทศ และสำนักหักบัญชี ซึ่งการเปิดเผยข้อมูลบุคคลส่วนบุคคลของท่านจะมีวัตถุประสงค์โดยเฉพาะเจาะจง ภายใต้ฐานทางกฎหมาย และมาตรการความปลอดภัยที่เหมาะสม<br>
    ง) บุคคลที่เกี่ยวข้องในกรณีการขายสิทธิเรียกร้อง และ/หรือ ทรัพย์สิน การปรับโครงสร้างองค์กร หรือการควบรวมกิจการของบริษัท ซึ่งบริษัทอาจต้องมีการโอนสิทธิไปยังกิจการดังกล่าว รวมถึงบุคคลต่าง ๆ ที่บริษัทจำเป็นต้องแบ่งปันข้อมูลเพื่อการขายสิทธิเรียกร้องและ/หรือทรัพย์สิน การปรับโครงสร้างองค์กร การโอนกิจการ ข้อตกลงทางการเงิน การจำหน่ายทรัพย์สิน หรือธุรกรรมอื่นใดที่เกี่ยวข้องกับกิจการ และ/หรือ ทรัพย์สินที่ใช้ในการดำเนินกิจการของบริษัท<br>
    จ) ธนาคารและสถาบันการเงินอื่น ๆ รวมทั้งบุคคลภายนอก ในกรณีที่มีกฎหมายกำหนดให้ต้องเปิดเผยข้อมูลเพื่อช่วยให้บุคคลดังกล่าวได้รับเงินคืนในกรณีที่มีการนำเงินเข้าบัญชีของท่านโดยผิดพลาด หรือการตรวจสอบเส้นทางการเงินในกรณีที่ท่านตกเป็นเหยื่ออาชญากรรมทางการเงิน หรือในกรณีที่มีเงินที่ต้องสงสัยเข้ามายังบัญชีของท่านจากอาชญากรรมทางการเงิน<br>
    ฉ) ตัวแทนทวงถามหนี้ ทนายความ บริษัทข้อมูลเครดิต หน่วยงานป้องกันการทุจริต ศาล หน่วยงาน หรือบุคคลใด ๆ ที่บริษัทถูกกำหนดหรือได้รับอนุญาตให้เปิดเผยข้อมูลส่วนบุคคลตามกฎหมาย กฎระเบียบ หรือคำสั่ง<br>
    ช) บุคคลภายนอกที่ให้บริการต่าง ๆ แก่บริษัท เช่น ผู้ให้บริการ customer service ผู้ให้บริการ IT service การวิเคราะห์และการเปรียบเทียบทางการตลาด ซึ่งรวมถึงแต่ไม่จำกัดเพียงตัวแทนธนาคารต่างประเทศ (correspondent banking) ตัวแทน หรือผู้รับเหมาช่วงที่กระทำการแทนบริษัท เช่น บริษัทที่จัดพิมพ์และนำส่งรายการบัตรเครดิต (credit card statements)<br>
    ซ) ผู้ให้บริการด้านสื่อสังคมออนไลน์ (ในรูปแบบที่ปลอดภัย) หรือบริษัทโฆษณาภายนอก เพื่อแสดงข้อความให้แก่ท่าน และบุคคลอื่นใดเกี่ยวกับผลิตภัณฑ์และบริการของบริษัท โดยบริษัทโฆษณาภายนอกอาจใช้ข้อมูลประวัติกิจกรรมออนไลน์ของท่าน เพื่อจัดสรรการโฆษณาที่ท่านอาจสนใจผู้ให้หลักประกันที่เป็นบุคคลภายนอก<br>
    ฌ) ผู้ให้หลักประกันที่เป็นบุคคลภายนอก<br>
    ญ) บุคคลอื่นใดที่ให้ผลประโยชน์หรือให้บริการที่เกี่ยวข้องกับผลิตภัณฑ์หรือบริการของท่าน<br>
    ฎ) ผู้รับมอบอำนาจ ผู้รับมอบอำนาจช่วง ตัวแทน หรือผู้แทนโดยชอบธรรมของท่านที่มีอำนาจตามกฎหมายโดยชอบ
    </p>
    <p>
    5. การส่งหรือโอนข้อมูลส่วนบุคคลของท่านไปยังต่างประเทศ<br>
    เนื่องจากกิจการในปัจจุบันเป็นธุรกิจที่ครอบคลุมทั่วโลก ในบางครั้งบริษัทจำเป็นต้องส่งหรือโอนข้อมูลส่วนบุคคลของท่านไปยังต่างประเทศ ในกรณีนี้บริษัทจะใช้ความพยายามอย่างดีที่สุดในการส่งหรือโอนข้อมูลส่วนบุคคลของท่านไปยังพันธมิตรทางธุรกิจ ผู้ให้บริการ หรือผู้รับข้อมูลของบริษัทที่มีความน่าเชื่อถือด้วยวิธีการที่ปลอดภัยที่สุดเพื่อรักษาความปลอดภัยของข้อมูลส่วนบุคคลของท่าน ซึ่งรวมถึงกรณีดังต่อไปนี้<br>
    ก) เป็นการปฏิบัติตามกฎหมาย<br>
    ข) ได้แจ้งให้ท่านทราบถึงมาตรฐานการคุ้มครองข้อมูลส่วนบุคคลที่ไม่เพียงพอของประเทศปลายทางและได้รับความยินยอมจากท่าน<br>
    ค) ปฏิบัติตามสัญญาที่ท่านทำไว้กับบริษัท หรือตามคำขอของท่านก่อนการเข้าทำสัญญา<br>
    ง) ปฏิบัติตามสัญญาระหว่างบริษัทกับบุคคลหรือนิติบุคคลอื่นเพื่อประโยชน์ของท่าน<br>
    จ) ป้องกันหรือระงับอันตรายต่อชีวิต ร่างกาย หรือสุขภาพของท่านหรือของบุคคลอื่น เมื่อท่านไม่สามารถให้ความยินยอมในขณะนั้นได้<br>
    ฉ) ดำเนินภารกิจเพื่อประโยชน์สาธารณะที่สำคัญ
    </p>
    <p>
    6. ระยะเวลาในการเก็บรักษาข้อมูลส่วนบุคคล<br>
    บริษัทจะเก็บรักษาข้อมูลส่วนบุคคลของท่านในระหว่างที่ท่านเป็นลูกค้าของบริษัทและเมื่อท่านสิ้นสุดความสัมพันธ์กับบริษัท (เช่น หลังจากที่ท่านปิดบัญชีที่มีอยู่กับบริษัท หรือนับแต่การทำธุรกรรมกับบริษัท หรือกรณีบริษัทปฏิเสธคำขอใช้บริการของท่าน หรือท่านขอยกเลิกการใช้บริการของบริษัท) บริษัทจะเก็บข้อมูลส่วนบุคคลของท่านตามระยะเวลาที่เหมาะสมและจำเป็นสำหรับข้อมูลส่วนบุคคลแต่ละประเภทและวัตถุประสงค์ตามที่พระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคลกำหนด<br>
