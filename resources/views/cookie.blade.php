    ข) เพื่อปฏิบัติตามกฎหมาย (เช่น กฎหมายธุรกิจสถาบันการเงิน กฎหมายป้องกันและปราบปรามการฟอกเงิน กฎหมายการป้องกันและปราบปรามการสนับสนุนทางการเงินแก่การก่อการร้ายและการแพร่ขยายอาวุธที่มีอานุภาพทำลายล้างสูง และกฎหมายอื่นที่บริษัทต้องปฏิบัติตาม ทั้งในประเทศไทยและต่างประเทศ<br>
    ค) เพื่อปฏิบัติตามกฎระเบียบและ/หรือคำสั่งของผู้มีอำนาจ (เช่น คำสั่งศาล คำสั่งของหน่วยงานรัฐ หน่วยงานที่มีหน้าที่กำกับดูแลบริษัท หรือพนักงานเจ้าหน้าที่ผู้มีอำนาจ)</p>
